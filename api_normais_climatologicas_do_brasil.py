from fastapi import FastAPI, HTTPException, Query
import pandas as pd
import uvicorn

# 1. Carregar o arquivo dados_climatologicos_processados.csv
# Certifique-se de que o caminho do arquivo está correto
df_clima_api = pd.read_csv('dados_climatologicos_processados.csv')

# 2. Tratar 'id_estacao' como string
df_clima_api['id_estacao'] = df_clima_api['id_estacao'].astype(str)

# 3. Inicializar a aplicação FastAPI
app = FastAPI(
    title="API de Normais Climatológicas do Brasil",
    description="API para servir dados de normais climatológicas processados do Brasil.",
    version="1.0.0"
)

# 4. Endpoint GET na rota raiz
@app.get("/", tags=["Root"])
async def read_root():
    return {"message": "Bem-vindo à API de Normais Climatológicas do Brasil"}

# 5. Endpoint GET na rota /clima para retornar todos os dados
@app.get("/clima", tags=["Dados Climatológicos"])
async def get_all_clima_data():
    return df_clima_api.to_dict(orient='records')

# 6. Endpoint GET na rota /clima/{station_id} para retornar dados de uma estação específica
@app.get("/clima/{station_id}", tags=["Dados Climatológicos"])
async def get_clima_by_station_id(station_id: str):
    station_data = df_clima_api[df_clima_api['id_estacao'] == station_id].to_dict(orient='records')
    if not station_data:
        raise HTTPException(status_code=404, detail=f"Estação com ID '{station_id}' não encontrada")
    return station_data

# 7. Endpoint GET na rota /clima/search com parâmetros de consulta opcionais
@app.get("/clima/search", tags=["Dados Climatológicos"])
async def search_clima_data(
    nome_estacao: str = Query(None, description="Filtrar por nome da estação (correspondência parcial)"),
    mes: int = Query(None, description="Filtrar por mês (1-12)")
):
    filtered_data = df_clima_api.copy()

    if nome_estacao:
        filtered_data = filtered_data[filtered_data['nome_estacao'].str.contains(nome_estacao, case=False, na=False)]

    if mes is not None:
        if not (1 <= mes <= 12):
            raise HTTPException(status_code=400, detail="Mês deve ser um valor entre 1 e 12")
        filtered_data = filtered_data[filtered_data['mes'] == mes]

    if filtered_data.empty and (nome_estacao or mes is not None):
        raise HTTPException(status_code=404, detail="Nenhum dado encontrado para os critérios de busca especificados")

    return filtered_data.to_dict(orient='records')

